using System;

namespace Calculation
{
    public class Calculate
    {
        public int Add(int s1, int s2)
        {
            return s1 + s2;
        }
        public int Subtract(int s1, int s2)
        {
            return s1 - s2;
        }
        public int Multiply(int s1, int s2)
        {
            return s1 * s2;
        }
        public int Divide(int s1, int s2)
        {
            return s1 / s2;
        }
    }
}
